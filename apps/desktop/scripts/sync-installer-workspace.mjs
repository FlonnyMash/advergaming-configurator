import { createRequire } from "node:module";
import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const {
  PROJECTS_DIR_NAME,
  WORKSPACE_DIR_NAME,
  STUDIO_ASSET_PROTOCOL,
} = require("../constants.js");

const desktopRoot = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
);
const outPath = path.join(desktopRoot, "nsis", "workspace-defines.nsh");

const contents = `; Auto-generated by scripts/sync-installer-workspace.mjs — do not edit.
!define WORKSPACE_DIR "${WORKSPACE_DIR_NAME}"
!define WORKSPACE_PROJECTS_DIR "${PROJECTS_DIR_NAME}"
!define STUDIO_ASSET_PROTOCOL "${STUDIO_ASSET_PROTOCOL}"
`;

writeFileSync(outPath, contents, "utf8");
console.log(`[sync-installer-workspace] wrote ${outPath}`);
